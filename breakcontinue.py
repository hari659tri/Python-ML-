# break statement is terminated the loop and continue statement is skip the current iteration and continue the next iteration
for i in range(210,220):
    if(i==215):
        continue
    print(i)
print("break statement")
for i in range(210,220):
    if(i==215):
        break
    print(i)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    